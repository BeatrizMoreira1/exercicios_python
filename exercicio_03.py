
celsius = float(input("Introduza a temperatura em graus Celsius: "))

fahrenheit = (celsius * 9/5) + 32

print(f"A temperatura em Fahrenheit é: {fahrenheit:.2f}")
#Em Python, um cálculo de adição é realizado utilizando o operador + para somar dois ou mais valores, sejam eles números inteiros, decimais ou variáveis que armazenam valores numéricos. O resultado da adição é então o valor resultante da soma desses números. 