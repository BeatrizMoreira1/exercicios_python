
numero = int(input("Digite um número inteiro: "))

if numero % 2 == 0:
    print(f"O número {numero} é par.")
else:
    print(f"O número {numero} é ímpar.")
#Em Python, o operador % calcula o resto da divisão inteira entre dois números. Ou seja, ele retorna o valor que sobra após dividir o primeiro número pelo segundo. É conhecido como o operador de módulo ou resto. 