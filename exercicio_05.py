
fahrenheit = float(input("Digite a temperatura em Fahrenheit: "))
  
celsius = (fahrenheit - 32) * 5/9

print(f"A temperatura em Celsius é: {celsius:.2f}°C")
#é um nome que serve como um rótulo para um valor armazenado na memória do computador
#use o código como referencia de como usar f string
#float define a variavel  como número decimal
 #format é um método de string usado para formatar strings, inserindo valores em posições específicas dentro de uma string